// 变量的解构赋值
// 解构定义： 从数组或者对象中提取值，再赋值给变量，这个过程成为解构
// 解读： 这种写法本质上属于模式匹配。（等号两边出现相同的模式时左边的变量将会被赋予对应的值）

var a1, a2, a3, a4, a5, a6, a7;
var f1 = function () {
    return 'f1 return';
};

[a1, a2, a3 = 11, a4 = 11, a5 = f1()] = [111, 222, null, undefined];
// 当赋值为null时，可以成功。
// 当赋值为undefined时，认为没有传递对应的值，赋值为默认值
// 默认值可以是函数

console.log(" || a1", a1, " || a2", a2, " || a3", a3, " || a4", a4, " || a5", a5);

